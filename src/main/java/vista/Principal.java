    void CargarTabla(){
        while(Modelo.getRowCount() > 0){
            Modelo.removeRow(0);
        }
        try {
               ResultSet rs = ControllerFacturas.CargarFacturas();
               while(rs.next()){
                   Object[] pValores = {rs.getString("idfactura"), rs.getString("numerofactura"), rs.getString("fecha"), rs.getString("tipodepago"), rs.getString("documentocliente"), rs.getString("nombrecliente"), rs.getString("subtotal"), rs.getString("descuento"), rs.getString("iva"), rs.getString("totaldescuento"), rs.getString("totalimpuesto"), rs.getString("total")};
                   Modelo.addRow(pValores);
               }
            } catch (Exception e) {
            }
    }
    
    void SeleccionarRegistro(){
        int Fila = jTable1.getSelectedRow();
        int idF = Integer.parseInt(jTable1.getValueAt(Fila, 0).toString());
        System.out.println(idF);
        try {
               ResultSet rs = ControllerFacturas.BuscarRegistro(idF);
               while(rs.next()){
                   System.out.println(rs.getString("numerofactura"));
                   
               }
            } catch (Exception e) {
            }
    }
    
    void Eliminar(){
        int Fila = jTable1.getSelectedRow();
        int idF = Integer.parseInt(jTable1.getValueAt(Fila, 0).toString());
        
        int i = JOptionPane.showConfirmDialog(null, "Desea eliminar la factura?");
        if (i==0){
            ControllerFacturas.EliminarRegistro(idF);
        }
    }
    
